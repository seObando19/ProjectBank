package com.example.projectbank;

public class cuenta {

    private String ident,nrocuent,saldo;

    public cuenta() {
    }

    public String getIdent() {
        return ident;
    }

    public void setIdent(String ident) {
        this.ident = ident;
    }

    public String getNrocuent() {
        return nrocuent;
    }

    public void setNrocuent(String nrocuent) {
        this.nrocuent = nrocuent;
    }

    public String getSaldo() {
        return saldo;
    }

    public void setSaldo(String saldo) {
        this.saldo = saldo;
    }
}
